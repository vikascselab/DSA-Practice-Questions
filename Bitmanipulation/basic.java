class basic {

  public static void main(String[] args) {
    System.out.println("welcome to bitmanipulation");

    System.out.println(5 & 6);
    System.out.println(5 | 6);
    System.out.println(5 ^ 6);
    System.out.println(~5);
    System.out.println(~0);
    System.out.println(5 << 2);
    System.out.println(6 >> 1);

  }

}
